import { useForm } from 'react-hook-form';
import { useMutation } from 'convex/react';
import { useAtomValue, useSetAtom } from 'jotai';
import { zodResolver } from '@hookform/resolvers/zod';

import { api } from '@workspace/backend/_generated/api'
import { Input } from '@workspace/ui/components/input';
import { Button } from '@workspace/ui/components/button';
import { Doc } from '@workspace/backend/_generated/dataModel';
import {
	Form,
	FormControl,
	FormField,
	FormItem,
	FormMessage,
} from '@workspace/ui/components/form';

import { WidgetHeader } from '@/modules/widget/ui/components/widget-header';
import { authFormSchema, authFormSchemaType } from '@/modules/widget/ui/schema';
import { contactSessionIdAtomFamily, organizationIdAtom, screenAtom } from '@/modules/widget/atoms/widget-atoms';

export const WidgetAuthScreen = () => {
	const setScreen = useSetAtom(screenAtom)

	const organizationId = useAtomValue(organizationIdAtom)
	const setContactSessionId = useSetAtom(contactSessionIdAtomFamily(organizationId || "")) 

	const form = useForm<authFormSchemaType>({
		resolver: zodResolver(authFormSchema),
		defaultValues: {
			name: '',
			email: '',
		},
	});

	const createContactSession = useMutation(api.public.contactSessions.create)

	const onSubmit = async (values: authFormSchemaType) => {
		if (!organizationId) {
			return;
		}

		const metadata: Doc<'contactSession'>['metadata'] = {
			userAgent: navigator.userAgent,
			language: navigator.language,
			languages: navigator.languages?.join(','),
			platform: navigator.platform,
			vendor: navigator.vendor,
			screenResolution: `${screen.width}x${screen.height}`,
			viewportSize: `${window.innerWidth}x${window.innerHeight}`,
			timezone: Intl.DateTimeFormat().resolvedOptions().timeZone,
			timezoneOffset: new Date().getTimezoneOffset(),
			cookieEnabled: navigator.cookieEnabled,
			referrer: document.referrer || 'direct',
			currentUrl: window.location.href,
		};

		const contactSessionId = await createContactSession({
			...values,
			metadata,
			organizationId,
		});

		setContactSessionId(contactSessionId);
		setScreen("selection")
	};

	return (
		<>
			<WidgetHeader>
				<div className="flex flex-col justify-between gap-y-2 px-2 py-6 font-semibold">
					<p className="text-3xl">Hi there! ðŸ‘‹</p>
					<p className="text-lg">Let&apos;s get you started</p>
				</div>
			</WidgetHeader>
			<Form {...form}>
				<form
					className="flex flex-1 flex-col gap-y-4 p-4"
					onSubmit={form.handleSubmit(onSubmit)}
				>
					<FormField
						control={form.control}
						name="name"
						render={({ field }) => (
							<FormItem>
								<FormControl>
									<Input
										className="h-10 bg-background"
										placeholder="e.g John Doe"
										type="text"
										{...field}
									/>
								</FormControl>
								<FormMessage />
							</FormItem>
						)}
					/>
					<FormField
						control={form.control}
						name="email"
						render={({ field }) => (
							<FormItem>
								<FormControl>
									<Input
										className="h-10 bg-background"
										placeholder="e.g john.doe@example.com"
										type="email"
										{...field}
									/>
								</FormControl>
								<FormMessage />
							</FormItem>
						)}
					/>
					<Button
						disabled={form.formState.isSubmitted}
						size="lg"
					>
						Continue
					</Button>
				</form>
			</Form>
		</>
	);
};
